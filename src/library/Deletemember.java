    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
           
        try{ 
            Mainclass mc=new Mainclass();
            
          //open connection
            Connection connection;
            connection=DriverManager.getConnection(mc.mydb,mc.uname,mc.upass);
            String str="";
            str="select name from staff where id =? ";
           PreparedStatement pst=connection.prepareStatement(str);
           pst.setString(1,id.getText());
           

           ResultSet rs;
           rs=pst.executeQuery();
           if (rs.next())
           {
                name.setText(rs.getString("name"));
                
                
            }
        }catch (Exception e)
        {
            System.err.println(e);
            System.exit(1);
        }
 

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         
        try {
            Mainclass ms=new Mainclass();
            Connection con =DriverManager.getConnection(ms.mydb,ms.uname,ms.upass);
            String sql="delete from staff where id=?";
            PreparedStatement ps= con.prepareStatement(sql);
            ps.setString(1,id.getText());
            ps.executeUpdate();
            
        }catch(SQLException e ){System.out.println(e.getMessage()); }  
        JOptionPane.showMessageDialog(null,"The requested member was Deleted");
    }//GEN-LAST:event_jButton2ActionPerformed

