    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        try
        {
            Mainclass mc=new Mainclass();
            
            Connection connection;          
            ResultSet rs;

            String StrQr="";
            if (accno.getText().trim().length()>0 )
            {
                StrQr=StrQr + " and accno = " + accno.getText().trim() + " ";
            }
            if (callno.getText().trim().length()>0 )
            {
                StrQr=StrQr + " and callno like '%" + callno.getText().trim() + "%' ";
            }            
            if (title.getText().trim().length()>0 )
            {
                StrQr=StrQr + " and title like '%" + title.getText().trim() + "%' ";
            } 
            
            if (StrQr.length()==0)
            {
               JOptionPane.showMessageDialog(null,"Enter search critaria.");
                return;
            }
            connection=DriverManager.getConnection(mc.mydb,mc.uname,mc.upass);
            PreparedStatement stmt=connection.prepareStatement("select  accno,callno,author,title,status from books where 1=1 " + StrQr + " order by accno");
            rs = stmt.executeQuery();
            int columns = rs.getMetaData().getColumnCount();
            while (rs.next())
            {  
                Object[] row = new Object[columns];
                for (int i = 1; i <= columns; i++)
                {  
                    row[i- 1] = rs.getObject(i); // 1
                }
                ((DefaultTableModel) jTable1.getModel()).insertRow(rs.getRow() - 1,row);
            }

        }
                catch (Exception e)
        {
            System.err.println(e);

        }

    }//GEN-LAST:event_searchActionPerformed

