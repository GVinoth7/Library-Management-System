    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
       
        try
       {
          
           
           Mainclass mc=new Mainclass();
            Connection connection = null;
            try {  
                connection=DriverManager.getConnection(mc.mydb,mc.uname,mc.upass);
            } catch (SQLException ex) {
                Logger.getLogger(Addbooks.class.getName()).log(Level.SEVERE, null, ex);
            }
            String sql = "insert into books(accno,callno,title,author) values (?,?,?,?) ";
            PreparedStatement pst=connection.prepareStatement(sql);
            pst.setString(1,bid.getText());
            pst.setString(2,callno.getText());
            pst.setString(3,title.getText());
            pst.setString(4,author.getText());
            int st=pst.executeUpdate();
            JOptionPane.showMessageDialog((null),"The Book was added to the database");
      
       }catch(SQLException e)
       {
           System.out.println(e.getMessage());
       }
        
        
    }//GEN-LAST:event_addActionPerformed

