    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
      
        
        
        
        try{ 
            Mainclass mc=new Mainclass();
            
             //open connection
            Connection connection;
            connection=DriverManager.getConnection(mc.mydb,mc.uname,mc.upass);
            String str="";
            str="select callno,title,author from books where accno =? ";
           PreparedStatement pst=connection.prepareStatement(str);
           pst.setString(1,bid.getText());
           

           ResultSet rs;
           rs=pst.executeQuery();
           if (rs.next())
           {
                 callno.setText(rs.getString("callno"));
                 bname.setText(rs.getString("title")); 
                 author.setText(rs.getString("author")); 

           }
        } catch (Exception e)
        {
            System.err.println(e);
            System.exit(1);
        }
 
       
    }//GEN-LAST:event_jButton3ActionPerformed

