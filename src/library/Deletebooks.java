    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      
        try {
            Mainclass ms=new Mainclass();
            Connection con =DriverManager.getConnection(ms.mydb,ms.uname,ms.upass);
            String sql="delete from books where accno=?";
            PreparedStatement ps= con.prepareStatement(sql);
            ps.setString(1,bid.getText());
            ps.executeUpdate();
            
        }catch(SQLException e ){System.out.println(e.getMessage()); }  
        JOptionPane.showMessageDialog(null,"The requested book was Deleted");
    }//GEN-LAST:event_jButton1ActionPerformed

